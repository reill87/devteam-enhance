/**
 * 단계별 연봉 (KRW 원 단위).
 * 풍자 톤이라 후반 단계는 비현실적 큰 수치.
 */
const SALARIES_KRW: number[] = [
  30_000_000,        // 0  코딩 부트캠프 수료생
  40_000_000,        // 1  인턴
  50_000_000,        // 2  신입
  55_000_000,        // 3  주니어
  60_000_000,        // 4  정규직 전환자
  80_000_000,        // 5  중급
  95_000_000,        // 6  선임
  110_000_000,       // 7  시니어
  130_000_000,       // 8  책임
  160_000_000,       // 9  테크리드
  200_000_000,       // 10 스태프
  250_000_000,       // 11 시니어 스태프
  350_000_000,       // 12 프린시플
  500_000_000,       // 13 디스팅귀시드
  800_000_000,       // 14 펠로우
  1_500_000_000,     // 15 CTO
  3_000_000_000,     // 16 전설의 풀스택
  8_000_000_000,     // 17 갓 개발자
  30_000_000_000,    // 18 코드의 신
  100_000_000_000,   // 19 알고리즘의 화신
  500_000_000_000,   // 20 컴파일러 그 자체
  1_000_000_000_000, // 21 우주의 운영체제
  100_000_000_000_000, // 22 튜링상 수상자 (100조원 — 농담)
];

/**
 * 단계가 SALARIES_KRW를 넘어서면 매 단계 ×3로 외삽.
 */
export function salaryAt(level: number): number {
  if (level < SALARIES_KRW.length) return SALARIES_KRW[level];
  const last = SALARIES_KRW[SALARIES_KRW.length - 1];
  return last * Math.pow(3, level - SALARIES_KRW.length + 1);
}
